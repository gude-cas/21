#include "ft_printf.h"

void	ft_putlstr(char *str, int *len)
{
	int	i;

	i = 0;
	if (!str)
	{
		*len += write(1, "(null)", 6);
		return ;
	}
	while (str[i])
	{
		write(1, &str[i], 1);
		*len += 1;
		i++;
	}
}
